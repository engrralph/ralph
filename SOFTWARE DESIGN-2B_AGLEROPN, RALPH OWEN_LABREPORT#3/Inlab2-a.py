stack = []

# append() function to push
# element in the stack
stack.append('q')
stack.append('r')
stack.append('s')

print('Initial stack')
print(stack)

# pop() function to pop
# element from stack in
# LIFO order
print('\nElements popped from stack:')
print(stack.pop())


print('\nStack after elements are popped:')
print(stack)